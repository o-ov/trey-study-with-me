def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # 检查是否已有下学期数据
    cur.execute("SELECT COUNT(*) FROM words WHERE unit_id LIKE '%-下'")
    existing = cur.fetchone()[0]
    if existing > 0:
        print(f"[import] 数据库已存在 {existing} 条下学期数据，跳过导入。")
        conn.close()
        return

    total = 0
    for unit_id, unit_name, words_str in UNIT_DATA:
        for word in words_str.split(","):
            word = word.strip()
            if not word:
                continue
            pinyin = PINYIN_MAP.get(word, "")
            cur.execute(
                "INSERT INTO words (char, pinyin, word, unit_id, unit_name) VALUES (?,?,NULL,?,?)",
                (word, pinyin, unit_id, unit_name)
            )
            total += 1

    conn.commit()

    # 验证
    cur.execute("SELECT COUNT(*) FROM words WHERE unit_id LIKE '%-下'")
    count = cur.fetchone()[0]
    cur.execute("SELECT DISTINCT unit_name FROM words WHERE unit_id LIKE '%-下' ORDER BY id")
    units = [r[0] for r in cur.fetchall()]
    conn.close()

    print(f"[import] ✅ 成功导入 {total} 条二年级下学期词条，覆盖 {len(units)} 个单元：{', '.join(units)}")


if __name__ == "__main__":
    main()
