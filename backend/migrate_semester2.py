def migrate():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    cur.execute("SELECT COUNT(*) FROM words WHERE unit_id LIKE '%-下'")
    count = cur.fetchone()[0]
    if count > 0:
        print(f"[migrate] 数据库已存在 {count} 条下学期数据，跳过。")
        conn.close()
        return

    inserted = 0
    for unit_id, unit_name, words_str in WORD_DATA:
        for word in words_str.split(","):
            word = word.strip()
            if not word:
                continue
            cur.execute(
                "INSERT INTO words (char, pinyin, word, unit_id, unit_name) VALUES (?,?,NULL,?,?)",
                (word, "", unit_id, unit_name)
            )
            inserted += 1

    conn.commit()
    conn.close()
    print(f"[migrate] ✅ 插入 {inserted} 条二年级下学期词条")


if __name__ == "__main__":
    migrate()
